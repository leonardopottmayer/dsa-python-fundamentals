# INTERVALS
print("INTERVALS")

# Prints numbers from 1 to 10
for i in range(1, 11):
    print(i)
    
# Printing even numbers between 50 and 101
for i in range(50, 102, 2):
    print(i)
    
# Printing even negative numbers between 0 and -20
for i in range(0, -20, -2):
    print(i)
    
# Using the length of a list
list = ["Apple", "Banana", "Orange", "Watermelon"]
length_of_the_list = len(list)
for i in range(0, length_of_the_list):
    print(list[i])
