# TYPE FUNCTION
print("TYPE FUNCTION")

# Int
print(type(5))

# Float
print(type(5.0))

# String
a = 'I\'m learning Python'
print(type(a))

# MATH OPERATIONS
print("MATH OPERATIONS")

# Float + float = float
print(3.1 + 6.4)

# Float + int = float
print(4 + 4.0)

# Int + int = int
print(4 + 4)

# Division between any type of numbers = float
print(4 / 2)

# Division between int and int = int (using // you can force the result to be an int)
print(4 // 2)

# Division between int and float = float (using // won't work if there' a float in the expression)
print (4 // 3.0)

# CONVERTING VALUES
print("CONVERTING VALUES")

# Convert int value to float
print(float(9))

# Convert float value to int
print(int(9.0))

# Convert float value to int
print(int(9.5))

# Hexadecimal and binary
print(hex(394))
print(hex(217))
print(bin(286))
print(bin(390))

# ABS, ROUND AND POW FUNCTIONS
print("ABS, ROUND AND POW FUNCTIONS")

# Returns the absolute value
print(abs(-8))

# Returns the absolute value
print(abs(8))

# Returns the value with 2 decimal places
print(round(3.141592653589793, 2))

# Potency
print(pow(4, 2))

# Potency
print(pow(5, 3))

# Square root
print(pow(64, 0.5))