print("Welcome to the parallelogram area calculator!")

# Ask the user to enter the length of the parallelogram base
base = float(input("Please enter the length of the parallelogram base: "))

# Ask the user to enter the height of the parallelogram
height = float(input("Please enter the height of the parallelogram: "))

# Calculate the area of the parallelogram
area = base * height

print("The area of the parallelogram is", area)